package ideator.action.evaluation;

import ideator.datamodel.IdeaFacade;

public class EvaluationResource {
	
	private Evaluation evaluation;
	private IdeaFacade idea;
	
	public EvaluationResource(Evaluation evaluation, IdeaFacade idea) {
		super();
		this.evaluation = evaluation;
		this.idea = idea;
	}
	
	public Evaluation getEvaluation() {
		return evaluation;
	}
	public void setEvaluation(Evaluation evaluation) {
		this.evaluation = evaluation;
	}
	public IdeaFacade getIdea() {
		return idea;
	}
	public void setIdea(IdeaFacade idea) {
		this.idea = idea;
	}
	
	

}
