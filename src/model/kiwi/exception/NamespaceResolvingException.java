package kiwi.exception;

/**
 * NamespaceResolvingException will be thrown when 
 * the system requires a namespace that does not exist at that moment.
 *  
 * @author Stephanie Stroka
 * 			(stephanie.stroka@salzburgresearch.at)
 *
 */
public class NamespaceResolvingException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8766744585599157067L;

	public NamespaceResolvingException() {
		super();
	}
	
	public NamespaceResolvingException(String msg) {
		super(msg);
	}
}
