function openEditPanel(id) {
	//back shadow
	var width = jQuery(document).width();
	var height = jQuery(document).height();
	jQuery("#back_shadow").css({width:width+"px",height:height+"px"});
	
	jQuery("#back_shadow").show();
	jQuery("#"+id).show();
}

function closeEditPanel(id) {
	jQuery("#"+id).hide();
	jQuery("#back_shadow").hide();
}

function closeBackground() {
	//jQuery("#back_shadow").fadeOut('slow');
	jQuery("#back_shadow").hide();
}